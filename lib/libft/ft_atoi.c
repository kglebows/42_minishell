#include "libft.h"

long	ft_atoi(const char *str)
{
	long		n;
	int			s;
	int			cnt;

	s = 1;
	n = 0;
	cnt = 0;
	while (*str == ' ' || *str == '\t' || *str == '\n' || *str == '\v'
		|| *str == '\f' || *str == '\r')
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			s = -1;
		str++;
	}
	while (ft_isdigit(*str))
	{
		n = n * 10 + (*str - '0');
		str++;
		cnt++;
	}
	if (cnt > 11)
		return (99999999999);
	return (n * s);
}
